#include <stdlib.h>


/**
 * add - add two integers 
 * @a: first integer
 * @b: second integer
 * return: results
 */
int add(int a, int b)
{
	int results;
	results = a + b;
	return (results);
}
/**
 * sub - subtract two integers
 * @a: first integer
 * @b: second integer
 * return: results
 */
int sub(int a, int b)
{
        int results;
        results = a - b;
        return (results);
}
/**
 * mul - multiplies two integers
 * @a: first integer
 * @b: second integer
 * return: results
 */
int mul(int a, int b)
{
        int results;
        results = a * b;
        return (results);
}
/**
 * mod - return modulus of two integers
 * @a: first integer
 * @b: second integer
 * return: results
 */
int mod(int a, int b)
{
        int results;
        results = a % b;
        return (results);
}
